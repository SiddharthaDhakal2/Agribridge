class HomeEntity {
	final String id;
	final String name;
	final double price;
	final int quantity;
	final String image;
	final String category;
	final String description;
	final String unit;
	final String availability;

	HomeEntity({
		required this.id,
		required this.name,
		required this.price,
		required this.quantity,
		required this.image,
		required this.category,
		required this.description,
		required this.unit,
		required this.availability,
	});
}
